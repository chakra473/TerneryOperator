public class StarPattern {
    public void star() {
        int i = 6, row = 6, j = 6;
        for (i = 0; i < row; i++) {
            {
                for (j = 0; j <= i; j++) {
                    System.out.println("* ");
                    System.out.println();
                }

            }


        }
    }
}


